package JMD_L2_Operations;

public class OperationsLogiques {
	
	public static void main(String[] args) {
		
		boolean X = true, Y = false;
        System.out.println("Pour X = true et Y = false :");
		System.out.println("X && Y (ET logique) : " + (X && Y));
		System.out.println("X || Y (OU logique) : " + (X || Y));
		System.out.println("!X (NON logique) : " + (!X));
    }
}
